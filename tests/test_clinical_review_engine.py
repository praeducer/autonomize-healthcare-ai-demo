from pathlib import Path

import pytest


@pytest.mark.unit
class TestApplicationSettings:
    """Tests for application_settings.py."""

    def test_settings_loads_defaults(self) -> None:
        """Settings should provide default values for all non-secret fields."""
        from prior_auth_demo.application_settings import ApplicationSettings

        settings = ApplicationSettings(
            anthropic_api_key="test-key-not-real",  # noqa: S106
            _env_file=None,
        )
        assert settings.claude_model_id == "claude-sonnet-4-6"
        assert settings.auto_approve_confidence_threshold == 0.85
        assert settings.human_review_confidence_threshold == 0.60
        assert settings.log_level == "INFO"

    def test_settings_requires_api_key(self, monkeypatch: pytest.MonkeyPatch) -> None:
        """Settings should raise ValidationError when ANTHROPIC_API_KEY is missing."""
        from pydantic import ValidationError

        from prior_auth_demo.application_settings import ApplicationSettings

        monkeypatch.delenv("ANTHROPIC_API_KEY", raising=False)
        with pytest.raises(ValidationError):
            ApplicationSettings(_env_file=None)


def _make_claim_response():  # noqa: ANN202
    """Helper to build a minimal valid FHIR ClaimResponse for tests."""
    from fhir.resources.R4B.claimresponse import ClaimResponse

    return ClaimResponse(
        status="active",
        type={
            "coding": [
                {
                    "system": "http://terminology.hl7.org/CodeSystem/claim-type",
                    "code": "professional",
                }
            ]
        },
        use="preauthorization",
        patient={"reference": "Patient/test-123"},
        insurer={"reference": "Organization/test-insurer"},
        outcome="complete",
        created="2026-03-24",
    )


@pytest.mark.unit
class TestClinicalReviewResultModel:
    """Tests for ClinicalReviewResult Pydantic model validation."""

    def test_model_validates_with_all_fields(self) -> None:
        """ClinicalReviewResult accepts all valid fields."""
        from prior_auth_demo.clinical_review_engine import ClinicalReviewResult

        result = ClinicalReviewResult(
            determination="APPROVED",
            confidence_score=0.92,
            clinical_rationale="Meets medical necessity criteria.",
            guideline_citations=["CMS LCD L12345"],
            missing_documentation=None,
            fhir_claim_response=_make_claim_response(),
            review_duration_seconds=12.5,
        )
        assert result.determination == "APPROVED"
        assert result.confidence_score == 0.92

    def test_model_rejects_invalid_determination(self) -> None:
        """ClinicalReviewResult rejects invalid determination values."""
        from pydantic import ValidationError

        from prior_auth_demo.clinical_review_engine import ClinicalReviewResult

        with pytest.raises(ValidationError):
            ClinicalReviewResult(
                determination="MAYBE",  # type: ignore[arg-type]
                confidence_score=0.5,
                clinical_rationale="test",
                guideline_citations=[],
                missing_documentation=None,
                fhir_claim_response=_make_claim_response(),
                review_duration_seconds=1.0,
            )

    def test_model_rejects_confidence_out_of_range(self) -> None:
        """ClinicalReviewResult rejects confidence scores outside 0.0-1.0."""
        from pydantic import ValidationError

        from prior_auth_demo.clinical_review_engine import ClinicalReviewResult

        with pytest.raises(ValidationError):
            ClinicalReviewResult(
                determination="APPROVED",
                confidence_score=1.5,
                clinical_rationale="test",
                guideline_citations=[],
                missing_documentation=None,
                fhir_claim_response=_make_claim_response(),
                review_duration_seconds=1.0,
            )

    def test_model_with_polyfactory(self) -> None:
        """ClinicalReviewResult can be generated by polyfactory."""
        from polyfactory.factories.pydantic_factory import ModelFactory

        from prior_auth_demo.clinical_review_engine import ClinicalReviewResult

        class ClinicalReviewResultFactory(ModelFactory):
            __model__ = ClinicalReviewResult
            fhir_claim_response = _make_claim_response()

        instance = ClinicalReviewResultFactory.build()
        assert instance.determination in (
            "APPROVED",
            "DENIED",
            "PENDED_FOR_REVIEW",
            "PENDED_MISSING_INFO",
        )
        assert 0.0 <= instance.confidence_score <= 1.0


