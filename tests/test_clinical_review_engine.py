@pytest.mark.unit
class TestRetrieveClinicalData:
    """Tests for extracting clinical data from FHIR Bundles."""

    def test_extracts_patient_from_bundle(self, sample_cases_dir: Path) -> None:
        """Clinical data extraction finds Patient resources in Bundle."""
        import json as json_mod

        from fhir.resources.R4B.bundle import Bundle

        from prior_auth_demo.clinical_review_engine import retrieve_clinical_data

        filepath = sample_cases_dir / "01_lumbar_mri_clear_approval.json"
        with filepath.open() as f:
            data = json_mod.load(f)
        bundle = Bundle.model_validate(data)
        result = retrieve_clinical_data(bundle)
        assert len(result["patients"]) >= 1
        assert len(result["conditions"]) >= 1
        assert len(result["supporting_info"]) >= 1

    def test_extracts_claim_details(self, sample_cases_dir: Path) -> None:
        """Clinical data extraction includes Claim details."""
        import json as json_mod

        from fhir.resources.R4B.bundle import Bundle

        from prior_auth_demo.clinical_review_engine import retrieve_clinical_data

        filepath = sample_cases_dir / "01_lumbar_mri_clear_approval.json"
        with filepath.open() as f:
            data = json_mod.load(f)
        bundle = Bundle.model_validate(data)
        result = retrieve_clinical_data(bundle)
        assert result["claim_details"].get("use") == "preauthorization"


