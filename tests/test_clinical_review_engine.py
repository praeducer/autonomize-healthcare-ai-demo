@pytest.mark.unit
class TestConfidenceRouting:
    """Tests for confidence-based determination routing logic."""

    def test_confidence_routing_auto_approve(self) -> None:
        """Confidence >= 0.85 with APPROVED determination stays APPROVED."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("APPROVED", 0.90, 0.85, 0.60)
        assert result == "APPROVED"

    def test_confidence_routing_high_confidence_denied_stays_denied(self) -> None:
        """DENIED determination is preserved regardless of confidence."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("DENIED", 0.95, 0.85, 0.60)
        assert result == "DENIED"

    def test_confidence_routing_low_confidence_approved_becomes_pended(self) -> None:
        """Confidence 0.60-0.84 with APPROVED routes to PENDED_FOR_REVIEW."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("APPROVED", 0.70, 0.85, 0.60)
        assert result == "PENDED_FOR_REVIEW"

    def test_confidence_routing_very_low_never_auto_denies(self) -> None:
        """Confidence < 0.60 with APPROVED routes to PENDED_FOR_REVIEW, never DENIED."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("APPROVED", 0.40, 0.85, 0.60)
        assert result == "PENDED_FOR_REVIEW"

    def test_confidence_routing_pended_missing_info_preserved(self) -> None:
        """PENDED_MISSING_INFO is always preserved regardless of confidence."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("PENDED_MISSING_INFO", 0.90, 0.85, 0.60)
        assert result == "PENDED_MISSING_INFO"

    def test_confidence_at_exact_threshold_approves(self) -> None:
        """Confidence exactly at 0.85 threshold should auto-approve."""
        from prior_auth_demo.clinical_review_engine import apply_confidence_routing

        result = apply_confidence_routing("APPROVED", 0.85, 0.85, 0.60)
        assert result == "APPROVED"


